# Program to show Constant Time Complexity

def printnumber(n):
    iterations=0
    print("The number entered by the user is:", n)
    iterations+=1
    print("Total iterations done by the code is ", iterations,"\n")


printnumber(10)
printnumber(20)
printnumber(30)
print("\nWith any 'n' the time taken by the code won't change" )








